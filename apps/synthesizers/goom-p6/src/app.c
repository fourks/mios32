/////////////////////////////////////////////////////////////////////////////
// This hook is called after startup to initialize the application
/////////////////////////////////////////////////////////////////////////////
void APP_Init(void)
{
  // initialize all LEDs
  MIOS32_BOARD_LED_Init(0xffffffff);

  // init P6 I/O scan
  MIOS32_BOARD_P6_ScanInit();

  // init Synth
  SYNTH_Init(0);
}


